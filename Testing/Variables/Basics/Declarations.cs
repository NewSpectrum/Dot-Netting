using System;

namespace DataTypes
{
	class Declarations
	{
		static void Main(string[] args);
		{
	/*===|  Declaration Examples  |===*/
		// Numeric Data Types
		int x = 75
		double y = 7.258
		long z = 67,893,342,128,382
		
		// Text-based Data Types
		string ChaCha = "I do the Cha-Cha like a Sissy Girl."
		char Nuts = 'n'
		
		// Boolean
		bool Result = true
		
	/*===|  Output Examples  |===*/
		// Numeric Outputs
		Console.WriteLine(x)
		Console.WriteLine(y)
		Console.WriteLine(z)
		
		Console.WriteLine(x * y)
		Console.WriteLine(x / y)
		
		// Text Outputs
		Console.WriteLine(ChaCha)
		Console.WriteLine(Nuts)
		
		// Text Outputs
		Console.WriteLine("Result: " + Result)
		}
	}
}